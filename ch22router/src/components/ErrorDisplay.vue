<template>
	<div class="bg-danger text-white text-center p-3 h3"
		v-if="error">
		An Error Has Occurred
		<h6>{{message}}</h6>
		<a href="/" class="btn btn-secondary">OK</a>
	</div>
</template>
<script>
export default {
	data(){
		return {
			error: false,
			message: ""
		}
	},
	methods:{
		handleError(err){
			this.error = true
			this.message = err
		}
	},
	inject: ["eventBus"],
	created(){
		this.eventBus.$on("httpError", this.handleError)
	}
}
</script>
